DROP VIEW IF EXISTS active_z CASCADE;
CREATE OR REPLACE 
	DEFINER = 'root'@'localhost'
VIEW active_z
AS
	select `zayavki`.`id` AS `id`,`nasel`.`nasname` AS `nasname`,`streets`.`nazv` AS `nazv`,`zayavki`.`n_dom` AS `n_dom`,`zayavki`.`noadr` AS `noadr`,`nowater`.`bez_v` AS `bez_v`,`users`.`fam` AS `res_fio`,`zayavki`.`tr_fio` AS `tr_fio`,`zayavki`.`status` AS `status`,`sp_work_type`.`id` AS `wtid`,`sp_work_type`.`w_type` AS `w_type`,`zayavki`.`inv_n` AS `inv_n`,`wd`.`works_desc` AS `works`,`zayavki`.`start_d` AS `start_d`,`zayavki`.`ceh` AS `ceh` from ((((((`zayavki` left join `nowater` on((`nowater`.`id` = `zayavki`.`id`))) left join `works_description` `wd` on((`wd`.`id` = `zayavki`.`id`))) join `nasel` on((`nasel`.`nasid` = `zayavki`.`nas`))) join `streets` on((`streets`.`id_ul` = `zayavki`.`ul`))) join `sp_work_type` on((`sp_work_type`.`id` = `zayavki`.`work_type`))) left join `users` on((`users`.`id` = `zayavki`.`user_id`))) where (`zayavki`.`status` = 1) order by `zayavki`.`id` desc;

