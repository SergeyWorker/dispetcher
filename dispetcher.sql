--
DROP PROCEDURE IF EXISTS getuser$$
CREATE DEFINER = 'root'@'localhost'
PROCEDURE getuser(IN inlogin varchar(15), IN pass varchar(32))
  READS SQL DATA
BEGIN
  DECLARE datacount int(1);
  SELECT
    COUNT(*) INTO datacount
  FROM users u
  WHERE u.l_name = inlogin
  AND u.u_pass = pass;
  IF datacount > 0
  THEN
    SELECT
      u.id,
      u.fam,
      u.ceh
    FROM users u
    WHERE u.l_name = inlogin
    AND u.u_pass = pass;
  END IF;
END
$$

