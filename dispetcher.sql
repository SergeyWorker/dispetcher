PROCEDURE get_last(IN nceh TINYINT, IN n_last_z INT)
BEGIN
  DECLARE big_last tinyint;
    SELECT count(*) INTO big_last FROM zayavki z WHERE ceh=nceh AND status=1 AND z.id>n_last_z;
  IF big_last>0 THEN
    CALL all_active_records_ceh(nceh);
  END IF;
END
$$

